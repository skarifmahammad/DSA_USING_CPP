#include <iostream>
using namespace std;
int main()
{
	int x, y;
	cout << "Enter 1st Number :";
	cin >> x;
	cout << "Enter 2nd Number :";
	cin >> y;
	cout << "X + y = " << x + y << endl;
	cout << "X - y = " << x - y << endl;
	cout << "X * y = " << x * y << endl;
	cout << "X / y = " << (float)x / y << endl;
	return 0;
}